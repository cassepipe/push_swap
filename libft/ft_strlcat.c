#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (i < size && dst[i])
		i++;
	i += ft_strlcpy(dst + i, src, size - i);
	return (i);
}
